#by using LIST methods

#by using APPEND methods
p=["guvi",2,3]
p.append("zoho")
print(p)

#by using CLEAR methods
a=["guvi",2,3]
a.clear()
print(a)

#by using COPY methods
b=["guvi",2,4]
b.copy()
print(b)

#by using COUNT methods
c=["guvi","2",3]
z=c.count(3)
print(z)

#by using EXTEND methods
x=["guvi","zoho"]
y=["ola","uber"]
x.extend(y)
print(x)

#by using INDEX methods
o=["ola","uber"]
s=o.index("uber")
print(s)

#by using INSERT methods
f=["guvi","zolo"]
f.insert(1,"zoho")
print(f)

#by using POP method
i=["cat","dog"]
i.pop(1)
print(i)

#by using REMOVE method
j=["cat","dog"]
j.remove("cat")
print(j)

#by using REVERSE method
g=["cat","dog"]
g.reverse()
print(g)

#by using SORT method
h=["cat","dog","amul","basic"]
h.sort()
print(h)